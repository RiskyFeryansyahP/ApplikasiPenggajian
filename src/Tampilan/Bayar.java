    public void ambilJabatan() {
        String query = "SELECT * from jabatan where id_jabatan=" + Gaji.jabatan + "";
        try {
            Statement stmt = conn.createStatement();
            ResultSet hasil = stmt.executeQuery(query);
            if (hasil.next()) {
                lblJabatan.setText(hasil.getString("nama_jabatan"));
            }
        } catch (Exception e) {
        }
    }

    public void cekJabatan() {
        if (lblJabatan.getText().equals("IT Support Officer")) {
            lbluangperjam.setText("500000");
        } else if (lblJabatan.getText().equals("Network Administrator")) {
            lbluangperjam.setText("700000");
        } else if (lblJabatan.getText().equals("Network Engineer")) {
            lbluangperjam.setText("800000");
        } else if (lblJabatan.getText().equals("IT Programmer")) {
            lbluangperjam.setText("900000");
        } else if (lblJabatan.getText().equals("Project Manager")) {
            lbluangperjam.setText("1000000");
        }
    }

