    private void btnOKActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnOKActionPerformed
        // TODO add your handling code here:
        if (txteterangan.getText().equals("") && !txtpotongan.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Tolong masukkan keterangan kenapa karyawan gajinya dipotong!");
        } 
        else if(!txteterangan.getText().equals("") && txtpotongan.getText().equals("")) {
            JOptionPane.showMessageDialog(null, "Tolong masukkan potongan jika anda memasukkan keterangan!");
        }
        else {
            if(txtpotongan.getText().equals(""))
            {
                potongan = 0;
            }
            else
            {
                potongan = Integer.parseInt(txtpotongan.getText());
            }
            int gaji = Integer.parseInt(lbluangperjam.getText());
            int jam_kerja = Integer.parseInt(txtjamkerja.getText());
            int gaji_pokok = gaji * jam_kerja;
            int gajiDiterima = gaji_pokok - potongan;
            lblgajiditerima.setText(Integer.toString(gajiDiterima));
            if (txtpotongan.getText().equals("") && txteterangan.getText().equals("")) {
                lblket.setText("");
            } else {
                lblket.setText("Gaji Karyawan Di Potong Sebesar " + potongan + " karena : " + txteterangan.getText() + " ");
            }

            String query = "INSERT INTO gaji VALUES(?,?,?,?,?)";
            try {
                PreparedStatement stmt = conn.prepareStatement(query);
                stmt.setString(1, lblIDkaryawan.getText());
                stmt.setString(2, txtjamkerja.getText());
                stmt.setString(3, lbluangperjam.getText());
                stmt.setString(4, Integer.toString(potongan));
                stmt.setString(5, txteterangan.getText());
                stmt.executeUpdate();

                JOptionPane.showMessageDialog(null, "Gaji Telah Dibayarkan");
            } catch (Exception e) {
                System.out.println("Gagal : " + e);
            }
        }
    }//GEN-LAST:event_btnOKActionPerformed

